define('FB_LOG','LOG');
define('FB_INFO','INFO');
define('FB_WARN','WARN');
define('FB_ERROR','ERROR');
define('FB_DUMP','DUMP');

/**
 * Sends the given data to FirePHP Firefox Extension.
 * The data can be displayed in the Firebug Console or in the
 * "Server" request tab.
 * 
 * Usage:
 * 
 * require('fb.php')
 * 
 * // NOTE: You must have Output Buffering enabled via
 * //       ob_start() or output_buffering ini directive.
 * 
 * fb('Hello World'); // Defaults to FB_LOG
 * 
 * fb('Log message'  ,FB_LOG);
 * fb('Info message' ,FB_INFO);
 * fb('Warn message' ,FB_WARN);
 * fb('Error message',FB_ERROR);
 * 
 * fb('Message with label','Label',FB_LOG);
 * 
 * fb(array('key1'=>'val1',
 *          'key2'=>array(array('v1','v2'),'v3')),
 *    'TestArray',FB_LOG);
 * 
 * function test($Arg1) {
 *   throw new Exception('Test Exception');
 * }
 * try {
 *   test(array('Hello'=>'World'));
 * } catch(Exception $e) {
 *   fb($e);
 * }
 * 
 * // Will show only in "Server" tab for the request
 * fb(apache_request_headers(),'RequestHeaders',FB_DUMP);
 * 
 * 
 * @param $Object Mixed  The message or object to be sent to FirePHP
 * @return Boolean  True if FirePHP was detected and headers were written, false otherwise
 * 
 * @version     0.1.1
 * @copyright   Copyright (c) 2008 FirePHP (http://www.firephp.org)
 * @author      Christoph Dorn <christoph@christophdorn.com>
 * @license     http://www.mozilla.org/MPL/
 * @link        http://www.firephp.org/
 */
function fb($Object) {

  if (headers_sent($filename, $linenum)) {
      throw new Exception('Headers already sent in '.$filename.' on line '.$linenum.'. Cannot send log data to FirePHP. You must have Output Buffering enabled via ob_start() or output_buffering ini directive.');
  }

  $Type = null;

  if(func_num_args()==1) {
  } else
  if(func_num_args()==2) {
    switch(func_get_arg(1)) {
      case FB_LOG:
      case FB_INFO:
      case FB_WARN:
      case FB_ERROR:
      case FB_DUMP:
        $Type = func_get_arg(1);
        break;
      default:
        $Object = array(func_get_arg(1),$Object);
        break;
    }
  } else
  if(func_num_args()==3) {
    $Type = func_get_arg(2);
    $Object = array(func_get_arg(1),$Object);
  } else {
    throw new Exception('Wrong number of arguments to fb() function!');
  }


  /* Check if FirePHP is installed on client */
  if(!preg_match_all('/\sFirePHP\/([\.|\d]*)\s?/si',$_SERVER['HTTP_USER_AGENT'],$m) ||
     !version_compare($m[1][0],'0.0.6','>=')) {
    return false;
  }

  if($Object instanceof Exception) {
    
    $Object = array('Class'=>get_class($Object),
                    'Message'=>$Object->getMessage(),
                    'File'=>$Object->getFile(),
                    'Line'=>$Object->getLine(),
                    'Trace'=>$Object->getTrace());
    if($Type===null) {
      $Type = 'TRACE';
    }
    
  } else {
    if($Type===null) {
      $Type = FB_LOG;
    }
  }

	header('X-FirePHP-Data-100000000001: {');
  if($Type==FB_DUMP) {
  	header('X-FirePHP-Data-200000000001: "FirePHP.Dump":{');
  	header('X-FirePHP-Data-299999999999: "__SKIP__":"__SKIP__"},');
  } else {
  	header('X-FirePHP-Data-300000000001: "FirePHP.Firebug.Console":[');
  	header('X-FirePHP-Data-399999999999: ["__SKIP__"]],');
  }
	header('X-FirePHP-Data-999999999999: "__SKIP__":"__SKIP__"}');

  $json = new FirePHP_JSON(SERVICES_JSON_LOOSE_TYPE);

  if($Type==FB_DUMP) {
  	$msg = '"'.$Object[0].'":'.$json->encode($Object[1]).',';
  } else {
  	$msg = '["'.$Type.'",'.$json->encode($Object).'],';
  }
 
	foreach( explode("\n",chunk_split($msg, 5000, "\n")) as $part ) {

		$mt = explode(' ',microtime());
		$mt = substr($mt[1],7).substr($mt[0],2);

		header('X-FirePHP-Data-'.(($Type==FB_DUMP)?'2':'3').$mt.': '.$part);
	}
  
  return true;
}


  
